def swap(a: int, b: int): # Se define la funcion que intercambia posiciones
    return b,a

def particionar(array, low, high):#Se define la funcion que divide las listas
    pivote = array[high] #El pivote sera el ultimo elemento de la lista
    i = low - 1 #'i' será 
        
    for j in range(low, high):
        #print(array[j])
        if array[j] <= pivote:
            i += 1
            #print(array[i])
            array[i], array[j] = swap(array[i], array[j])
    
    array[i + 1], array[high] = swap(array[i + 1], array[high] )
    
    return i + 1
    
def quick_sort(array, low, high):
    global c
    c += 1
    if low < high:
        pi = particionar(array, low, high)
        quick_sort(array, low, pi - 1)
        quick_sort(array, pi + 1, high)
        

